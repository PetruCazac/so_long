char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*l;
	char	a;

	i = 0;
	l = 0;
	a = (char) c;
	while (s[i] && s[i] != a)
		i++;
	if (s[i] == a)
		return ((char *) &s[i]);
	return (l);
}
