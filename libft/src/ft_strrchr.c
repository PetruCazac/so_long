char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*l;
	char	a;

	i = 0;
	l = 0;
	a = (char) c;
	while (s[i])
	{
		if (s[i] == a)
			l = (char *) s + i;
		i++;
	}
	if (a == s[i])
		l = (char *) s + i;
	return (l);
}
